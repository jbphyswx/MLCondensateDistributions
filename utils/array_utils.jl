"""
    conv3d_valid_box_mean!(out, data, wx, wy, wz; stride_h=1, stride_v=1, stride_z=1)

Valid (non-padded) 3D box average: each output cell `(io,jo,ko)` averages
`data[i0:(i0+wx-1), j0:(j0+wy-1), k0:(k0+wz-1)]` with
`i0 = 1 + (io-1)*stride_h`, etc.
"""
function conv3d_valid_box_mean!(
    out::AbstractArray{T, 3},
    data::AbstractArray{T, 3},
    wx::Int,
    wy::Int,
    wz::Int;
    stride_h::Int = 1,
    stride_v::Int = 1,
    stride_z::Int = 1,
) where {T <: Real}
    wx >= 1 && wy >= 1 && wz >= 1 || throw(ArgumentError("window sizes must be >= 1"))
    stride_h >= 1 && stride_v >= 1 && stride_z >= 1 || throw(ArgumentError("strides must be >= 1"))
    nx, ny, nz = size(data)
    nxo = valid_box_output_extent(nx, wx, stride_h)
    nyo = valid_box_output_extent(ny, wy, stride_v)
    nzo = valid_box_output_extent(nz, wz, stride_z)
    (nxo == 0 || nyo == 0 || nzo == 0) && throw(DimensionMismatch("window too large for data"))
    size(out) == (nxo, nyo, nzo) || throw(DimensionMismatch("out size mismatch for conv3d_valid_box_mean"))
    inv_vol = one(T) / T(wx * wy * wz)
    if nthreads() > 1 && nxo * nyo * nzo >= 2048
        @threads for io in 1:nxo
            i0 = 1 + (io - 1) * stride_h
            @inbounds for ko in 1:nzo
                k0 = 1 + (ko - 1) * stride_z
                for jo in 1:nyo
                    j0 = 1 + (jo - 1) * stride_v
                    acc = zero(T)
                    for kk in 0:(wz - 1)
                        z = k0 + kk
                        for jj in 0:(wy - 1)
                            y = j0 + jj
                            for ii in 0:(wx - 1)
                                x = i0 + ii
                                acc += data[x, y, z]
                            end
                        end
                    end
                    out[io, jo, ko] = acc * inv_vol
                end
            end
        end
    else
        @inbounds for ko in 1:nzo
            k0 = 1 + (ko - 1) * stride_z
            for jo in 1:nyo
                j0 = 1 + (jo - 1) * stride_v
                for io in 1:nxo
                    i0 = 1 + (io - 1) * stride_h
                    acc = zero(T)
                    for kk in 0:(wz - 1)
                        z = k0 + kk
                        for jj in 0:(wy - 1)
                            y = j0 + jj
                            for ii in 0:(wx - 1)
                                x = i0 + ii
                                acc += data[x, y, z]
                            end
                        end
                    end
                    out[io, jo, ko] = acc * inv_vol
                end
            end
        end
    end
    return out
end

function conv3d_valid_box_mean(
    data::AbstractArray{T, 3},
    wx::Int,
    wy::Int,
    wz::Int;
    stride_h::Int = 1,
    stride_v::Int = 1,
    stride_z::Int = 1,
) where {T <: Real}
    nx, ny, nz = size(data)
    nxo = valid_box_output_extent(nx, wx, stride_h)
    nyo = valid_box_output_extent(ny, wy, stride_v)
    nzo = valid_box_output_extent(nz, wz, stride_z)
    (nxo == 0 || nyo == 0 || nzo == 0) && throw(DimensionMismatch("window too large for data"))
    out = similar(data, nxo, nyo, nzo)
    return conv3d_valid_box_mean!(out, data, wx, wy, wz; stride_h, stride_v, stride_z)
end

function conv3d_valid_box_product_mean!(
    out::AbstractArray{T, 3},
    x::AbstractArray{T, 3},
    y::AbstractArray{T, 3},
    wx::Int,
    wy::Int,
    wz::Int;
    stride_h::Int = 1,
    stride_v::Int = 1,
    stride_z::Int = 1,
) where {T <: Real}
    size(x) == size(y) || throw(DimensionMismatch("x and y must match shape"))
    wx >= 1 && wy >= 1 && wz >= 1 || throw(ArgumentError("window sizes must be >= 1"))
    stride_h >= 1 && stride_v >= 1 && stride_z >= 1 || throw(ArgumentError("strides must be >= 1"))
    nx, ny, nz = size(x)
    nxo = valid_box_output_extent(nx, wx, stride_h)
    nyo = valid_box_output_extent(ny, wy, stride_v)
    nzo = valid_box_output_extent(nz, wz, stride_z)
    (nxo == 0 || nyo == 0 || nzo == 0) && throw(DimensionMismatch("window too large for data"))
    size(out) == (nxo, nyo, nzo) || throw(DimensionMismatch("out size mismatch"))
    inv_vol = one(T) / T(wx * wy * wz)
    if nthreads() > 1 && nxo * nyo * nzo >= 2048
        @threads for io in 1:nxo
            i0 = 1 + (io - 1) * stride_h
            @inbounds for ko in 1:nzo
                k0 = 1 + (ko - 1) * stride_z
                for jo in 1:nyo
                    j0 = 1 + (jo - 1) * stride_v
                    acc = zero(T)
                    for kk in 0:(wz - 1)
                        z = k0 + kk
                        for jj in 0:(wy - 1)
                            yy = j0 + jj
                            for ii in 0:(wx - 1)
                                xi = i0 + ii
                                acc += x[xi, yy, z] * y[xi, yy, z]
                            end
                        end
                    end
                    out[io, jo, ko] = acc * inv_vol
                end
            end
        end
    else
        @inbounds for ko in 1:nzo
            k0 = 1 + (ko - 1) * stride_z
            for jo in 1:nyo
                j0 = 1 + (jo - 1) * stride_v
                for io in 1:nxo
                    i0 = 1 + (io - 1) * stride_h
                    acc = zero(T)
                    for kk in 0:(wz - 1)
                        z = k0 + kk
                        for jj in 0:(wy - 1)
                            yy = j0 + jj
                            for ii in 0:(wx - 1)
                                xi = i0 + ii
                                acc += x[xi, yy, z] * y[xi, yy, z]
                            end
                        end
                    end
                    out[io, jo, ko] = acc * inv_vol
                end
            end
        end
    end
    return out
end

function conv3d_valid_box_product_mean(
    x::AbstractArray{T, 3},
    y::AbstractArray{T, 3},
    wx::Int,
    wy::Int,
    wz::Int;
    stride_h::Int = 1,
    stride_v::Int = 1,
    stride_z::Int = 1,
) where {T <: Real}
    nx, ny, nz = size(x)
    nxo = valid_box_output_extent(nx, wx, stride_h)
    nyo = valid_box_output_extent(ny, wy, stride_v)
    nzo = valid_box_output_extent(nz, wz, stride_z)
    out = similar(x, nxo, nyo, nzo)
    return conv3d_valid_box_product_mean!(out, x, y, wx, wy, wz; stride_h, stride_v, stride_z)
end

"""
    coarsen_fields_horizontal(fields, fx, fy)

Coarsen all 3D fields in `fields` with a shared `(fx, fy)`.
All fields must share the same size.
"""
function coarsen_fields_horizontal(fields::AbstractDict{K, A}, fx::Int, fy::Int) where {K, T <: Real, A <: AbstractArray{T, 3}}
    isempty(fields) && return Dict{K, A}()

    first_key = first(keys(fields))
    base_size = size(fields[first_key])
    for (name, arr) in pairs(fields)
        size(arr) == base_size || throw(DimensionMismatch("Field $(name) has size $(size(arr)) but expected $(base_size)"))
    end

    out = Dict{K, Array{T, 3}}()
    for (name, arr) in pairs(fields)
        out[name] = Array(coarsen3d_horizontal_mean(arr, fx, fy))
    end
    return out
end

